				<li>В качестве материала для сооружения камина.</li>
				<li>Для мощения пола и пешеходных дорожек.</li>
				<li>Для декорирования и облицовки стен.</li>
				<li>Для постройки колонн, декоративных фантанов и ступеней.</li>
				<li>Для создания искусственных скал и приподнятых цветочных клумб.</li>
				</ul>
				
