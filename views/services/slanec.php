				<li>Горючие – легковоспламеняющийся камень, похожий на застывшую глину.</li>
				<li>Хлоритовые – зеленый сланец с ярким блеском.</li>
				</ul>
		
				<h2>Сфера применения</h2>
				<p>Сланец относительно дорогой природный камень. Но физические и химические свойства, вкупе с необычным внешним видом позволяют сланцевой плитке быть одним из лидеров среди материалов, которые используют в работах по оформлению ландшафтного дизайна.</p>
				<p>Сланец обычно применяется:</p>
