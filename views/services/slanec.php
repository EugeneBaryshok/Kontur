					<h2 style="font-size: 30px">  Виды сланца</h2>
				<p>Сланцы обычно делятся по цвету и химическому составу. Расцветка сланцевых плиток разнообразна и зависит от структуры камня и его органического состава. Популярными и востребованными считаются черные и серые сланцы. Они имеют выраженные графитовый оттенок с матовым блеском.</p>
				<p>По своему химическому составу сланцы делятся на следующие виды:</p>
